#include "3-calc.h"
/**
* op_add - Makes the sum of a and b.
* @a: First number.
* @b: Second number.
* Return: The sum of a and b.
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - Makes the difference of a and b.
* @a: First number.
* @b: Second number.
* Return: The difference of a and b.
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - Makes the product of a and b.
* @a: First number.
* @b: Second number.
* Return: The product of a and b.
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - Makes the division of a and b.
* @a: First number.
* @b: Second number.
* Return: The result of the division of a and b.
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - Makes the module of a and b.
* @a: First number.
* @b: Second number.
* Return: The remainder of the dicision of a and b.
*/
int op_mod(int a, int b)
{
	return (a % b);
}
